    private void refreshView () {
        
        //Activando "agregar jugadores"
        if (teamController.hasTeams()) {
            lblError.setVisible(false);
            btnAdd.setEnabled(true);
        }
        
        //Actualizando tabla
        tblPlayers.setModel(playerController.getPlayers());
        
        //Actualizando rankings
        String[] stadistics = playerController.getStadistics();
        
        lblHighest.setText(stadistics[0]);
        lblYoungest.setText(stadistics[1]);
        lblHeaviest.setText(stadistics[2]);
        
        //Actualizando lista de equipos
        DefaultComboBoxModel cmbModel = teamController.getTeams();
        cmbTeam.setModel(cmbModel);
        cmbModel = teamController.getTeams();
        cmbTeam1.setModel(cmbModel);
        cmbModel = teamController.getTeams();
        cmbTeam2.setModel(cmbModel);
        
    }
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String name = txtName.getText().trim();
            int age = Integer.parseInt(txtAge.getText().trim());
            double height = Double.parseDouble(txtHeight.getText().trim());
            double weight = Double.parseDouble(txtWeight.getText().trim());
            Team team = (Team) cmbTeam.getSelectedItem();
            
            //A√±adiendo jugador a lista de jugadores
            Player player = playerController.addPlayer(name, age, height, weight);
            String result = "";
            
            if (player != null) {
                //A√±adiendo jugador a equipo
                teamController.addPlayer(team, player);
                
                result = "Jugador a√±adido exitosamente";
                txtName.setText("");
                txtAge.setText("");
                txtHeight.setText("");
                txtWeight.setText("");
                refreshView();
            }
            else 
                result = "Ocurri√≥ un error.";
            
            JOptionPane.showMessageDialog(this, result);
            
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos con datos v√°lidos.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnAddTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTeamActionPerformed
        try {
            String name = txtTeam.getText().trim();
            
            if (name.equals(""))
                JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos con datos v√°lidos.");
            else{
               
                String response = teamController.addTeam(name);
                String result = "";

                switch (response) {
                    case "OK":
                        result = "Equipo a√±adido exitosamente";
                        txtTeam.setText("");
                        refreshView();
                        break;
                    case "ERROR":
                        result = "Ocurri√≥ un error.";
                        break;
                }

                JOptionPane.showMessageDialog(this, result);
            }
            
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos con datos v√°lidos.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddTeamActionPerformed

    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        try {
            
            Team team1 = (Team)cmbTeam1.getSelectedItem();
            Team team2 = (Team)cmbTeam2.getSelectedItem();

            if (team1 == team2) 
                JOptionPane.showMessageDialog(this, "Debe seleccionar dos equipos diferentes.");

            else if (!teamController.hasPlayers(team1) || !teamController.hasPlayers(team2))
                JOptionPane.showMessageDialog(this, "Debe seleccionar equipos con integrantes.");

            else {
                tblTeam1.setModel(teamController.getPlayers((Team)cmbTeam1.getSelectedItem()));
                tblTeam2.setModel(teamController.getPlayers((Team)cmbTeam2.getSelectedItem()));
                JOptionPane.showMessageDialog(this, "Partido realizado.");
            }
        }
        
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay equipos registrados.");
        }
    }//GEN-LAST:event_btnPlayActionPerformed

